/* ══════════════════════════════════════════
   Good Days 갤러리 LQIP blurDataURL 사전 생성 (S121)

   plaiceholder 로 42장 webp 의 base64 blurDataURL 을 생성하고
   `src/lib/gooddaysBlur.ts` 로 export. 빌드 타임 1회 실행.

   실행: npx tsx scripts/generate-gallery-blur.ts
   ══════════════════════════════════════════ */

import { promises as fs } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { getPlaiceholder } from 'plaiceholder';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const NEXT_ROOT = path.join(__dirname, '..');
const PUBLIC_DIR = path.join(NEXT_ROOT, 'public');
const GALLERY_DIR = path.join(PUBLIC_DIR, 'images', 'gallery');
const OUTPUT_FILE = path.join(NEXT_ROOT, 'src', 'lib', 'gooddaysBlur.ts');

async function main(): Promise<void> {
  const files = (await fs.readdir(GALLERY_DIR))
    .filter((f) => f.endsWith('.webp'))
    .sort();

  if (files.length === 0) {
    throw new Error(`No webp files in ${GALLERY_DIR}`);
  }

  console.log(`[blur] generating LQIP for ${files.length} images...`);

  const entries: { src: string; blurDataURL: string }[] = [];
  for (const file of files) {
    const filePath = path.join(GALLERY_DIR, file);
    const buffer = await fs.readFile(filePath);
    const { base64 } = await getPlaiceholder(buffer, { size: 10 });
    entries.push({
      src: `/images/gallery/${file}`,
      blurDataURL: base64,
    });
    process.stdout.write('.');
  }
  process.stdout.write('\n');

  const header = `/* AUTO-GENERATED by scripts/generate-gallery-blur.ts — DO NOT EDIT MANUALLY.
   Re-generate via: npx tsx scripts/generate-gallery-blur.ts
   Source: public/images/gallery/*.webp (${files.length} files) */
`;

  const body = `export const GD_BLUR_MAP: Record<string, string> = ${JSON.stringify(
    Object.fromEntries(entries.map((e) => [e.src, e.blurDataURL])),
    null,
    2,
  )};
`;

  await fs.writeFile(OUTPUT_FILE, header + '\n' + body, 'utf8');
  console.log(`[blur] wrote ${OUTPUT_FILE}`);
  const totalBytes = entries.reduce((sum, e) => sum + e.blurDataURL.length, 0);
  console.log(`[blur] payload total ≈ ${(totalBytes / 1024).toFixed(1)} KB`);
}

main().catch((err) => {
  console.error('[blur] FAILED:', err);
  process.exit(1);
});
