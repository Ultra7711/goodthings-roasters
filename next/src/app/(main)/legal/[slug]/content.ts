export type LegalSection = {
  heading?: string;
  paragraphs?: string[];
  /** 들여쓰기 1단계 — 항목 리스트 */
  bullets?: string[];
  /** 들여쓰기 2단계 — 정의 리스트 (라벨 · 값 · 선택적 외부 링크).
   *  linkOn: link 가 걸리는 위치. 'label' (default) — 기관명/구분이 link.
   *  'value' — 회사명/주체명 등 value 자체가 의미를 가질 때 link. */
  definitions?: { label: string; value: string; link?: string; linkOn?: 'label' | 'value' }[];
};

export type LegalDoc = {
  slug: string;
  title: string;
  /** SEO meta description (160자 이내) */
  description: string;
  /** 시행일 등 부제 (선택) */
  effectiveDate?: string;
  intro?: string[];
  sections: LegalSection[];
  /** 페이지 하단 사업자 표기 (선택) */
  footer?: string[];
  /** 정보·도움말 페이지에서 섹션 단위 아코디언 사용 (heading 단위로 그룹화) */
  useAccordion?: boolean;
};

export const LEGAL_SLUGS = [
  'terms',
  'privacy',
  'business-info',
  'shipping',
  'returns',
  'payment-faq',
] as const;

export type LegalSlug = (typeof LEGAL_SLUGS)[number];

export function isLegalSlug(value: string): value is LegalSlug {
  return (LEGAL_SLUGS as readonly string[]).includes(value);
}

