    '배경 이미지들은 동일한 시즌 배너의 BP 변형이므로 색감·톤이 **최대한 동일하게 보여야** 합니다.',
    '별개 이미지가 아니라 "같은 사진의 BP 자동 크롭" 처럼 보이는 게 목표.',
